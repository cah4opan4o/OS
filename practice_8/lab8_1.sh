# 1. Вывести информацию обо всех процессах системы
# и подсчитать общее количество процессов.
echo "Общее количество процессов в системе:"
ps -e | wc -l

# Подсчет процессов, принадлежащих текущему пользователю
echo "Количество процессов, принадлежащих вам:"
ps -u $USER | wc -l

# 2. Получить дерево процессов, принадлежащих вашему сеансу
echo "Дерево процессов вашего сеанса:"
ps -u $USER -H

# 3. Перенаправление дерева процессов в файл ~/pstrees
pstree > ~/pstrees
echo "Дерево процессов записано в файл ~/pstrees."
echo "Содержимое файла ~/pstrees:"
cat ~/pstrees

# Анализ результата работы команды pstree
echo "Сравните результаты с предыдущим выводом."

# 4. Описать наиболее активные процессы в системе
echo "Наиболее активные процессы в системе:"
top -b -n 1 | head -n 20

# 5. Выполнить команду man в фоновом режиме
man bash &

# 6. Посмотреть состояние фоновых задач
echo "Состояние фоновых задач:"
jobs

# 7. Перевести задачу man в интерактивный режим (если она все еще выполняется)
# Чтобы сделать это, сначала мы используем команду fg для задачи, которая будет известна по её номеру
jobs
echo "Перевожу задачу man в интерактивный режим:"
fg %1
